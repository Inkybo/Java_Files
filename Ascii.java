public class Ascii
{
	public static void main(String[] args)
	{
			char x = ' ';
						
			int z = 0;
		
			
			x = 'A';
			z = 70;
			
			System.out.println("The letter A has this ascii value: " + (int)x);
			
			System.out.println("The number 70 is the ascii value of this letter: " + (char)(z));
			
			System.out.println ("smiley: " + (char)(1));
			
			int b = 0;
			b = 'f';    // not an error.
			System.out.println ("the ascii value of f is " + b);
	}
}
		
		